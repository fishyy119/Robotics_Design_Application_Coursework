function color_value = tab10Color(color_index, alpha_value)
% 返回 Matplotlib 默认 Tab10 调色盘中的指定颜色。

palette = [
    0.121568627450980, 0.466666666666667, 0.705882352941177;
    1.000000000000000, 0.498039215686275, 0.054901960784314;
    0.172549019607843, 0.627450980392157, 0.172549019607843;
    0.839215686274510, 0.152941176470588, 0.156862745098039;
    0.580392156862745, 0.403921568627451, 0.741176470588235;
    0.549019607843137, 0.337254901960784, 0.294117647058824;
    0.890196078431372, 0.466666666666667, 0.760784313725490;
    0.498039215686275, 0.498039215686275, 0.498039215686275;
    0.737254901960784, 0.741176470588235, 0.133333333333333;
    0.090196078431373, 0.745098039215686, 0.811764705882353
];

palette_size = size(palette, 1);
palette_index = mod(color_index - 1, palette_size) + 1;
color_value = palette(palette_index, :);

if nargin >= 2 && ~isempty(alpha_value)
    color_value = [color_value, alpha_value];
end
end
